#example of relational operators 
#1st we accept two input from user 
num1 = 10 
num2 = 11

#check whether num1 and num2 has same value, True or False 
result = num1 == num2 
print(f"{result} = {num1} == {num2}")

#check whether num1 and num2 has different value, True or False 
result = num1 != num2 
print(f"{result} = {num1} != {num2}")

#check whether num1 is less then num2, True or False 
result = num1 < num2 
print(f"{result} = {num1} < {num2}")

#check whether num1 is greater then num2, True or False 
result = num1 > num2 
print(f"{result} = {num1} > {num2}")

#check whether num1 is less then or equal to num2, True or False 
result = num1 <= num2 
print(f"{result} = {num1} <= {num2}")

#check whether num1 is greater then or equal to num2, True or False 
result = num1 >= num2 
print(f"{result} = {num1} >= {num2}")
